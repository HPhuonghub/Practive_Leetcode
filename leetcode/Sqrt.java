package leetcode;

public class Sqrt {
    public static void main(String[] args) {
        System.out.println(sqrt(3));
        System.out.println(sqrt(4));
        System.out.println(sqrt(8));
        System.out.println(sqrt(-5));
    }

    static int sqrt(int x) {
        return (int)Math.sqrt(x);
    }
}
